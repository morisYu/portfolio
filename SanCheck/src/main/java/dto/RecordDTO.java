package dto;

public class RecordDTO {
	private int record_no;
	private String record_nickname;
	private String record_title;
	private String record_content;
	private String record_photo;
	private int record_nice;
	private String record_reg_date;
	private int record_open;
	private String record_ip;
	
	public int getRecord_no() {
		return record_no;
	}
	public void setRecord_no(int record_no) {
		this.record_no = record_no;
	}
	public String getRecord_nickname() {
		return record_nickname;
	}
	public void setRecord_nickname(String record_nickname) {
		this.record_nickname = record_nickname;
	}
	public String getRecord_title() {
		return record_title;
	}
	public void setRecord_title(String record_title) {
		this.record_title = record_title;
	}
	public String getRecord_content() {
		return record_content;
	}
	public void setRecord_content(String record_content) {
		this.record_content = record_content;
	}
	public String getRecord_photo() {
		return record_photo;
	}
	public void setRecord_photo(String record_photo) {
		this.record_photo = record_photo;
	}
	public int getRecord_nice() {
		return record_nice;
	}
	public void setRecord_nice(int record_nice) {
		this.record_nice = record_nice;
	}
	public String getRecord_reg_date() {
		return record_reg_date;
	}
	public void setRecord_reg_date(String record_reg_date) {
		this.record_reg_date = record_reg_date;
	}
	public int getRecord_open() {
		return record_open;
	}
	public void setRecord_open(int record_open) {
		this.record_open = record_open;
	}
	public String getRecord_ip() {
		return record_ip;
	}
	public void setRecord_ip(String record_ip) {
		this.record_ip = record_ip;
	}
}
